# - 大段目录树字符（├── └── │）
# - 纯陈述无指令动作（不含"你"、"请"、"做"、"按"、"应"等指令词）

_DOC_CHAPTER_TITLES = (
    # AGENTS.md / Cursor Rules 标准章节
    "coding style", "naming conventions", "project structure",
    "module organization", "build commands", "test commands",
    "development commands", "testing guidelines", "commit guidelines",
    "pull request guidelines", "security configuration", "security tips",
    "configuration tips", "code style", "style guide",
    # 项目文档常见章节
    "项目结构", "项目概述", "项目方法论", "目录结构", "组件清单",
    "关键文档", "关键纪律", "关键约束", "关键决策", "工作流",
    "环境变量", "部署", "运维", "高频陷阱", "兼容性",
    "构建", "测试配置", "端口分配",
    # README 类章节
    "introduction", "overview", "getting started", "installation",
    "usage", "license", "contributing",
    "简介", "介绍", "安装", "使用", "许可", "贡献",
)

_DOC_PREFIX_PHRASES = (
    "Core package code lives in", "Use Python", "Tests use", "Prefer ",
    "Top-level regression tests", "Generated outputs should stay",
    "Supporting research material", "This file provides",
    "These rules apply to", "Recent history uses",
)

_TREE_CHARS_RE = re.compile(r"[├└│─]")
_IMPERATIVE_WORDS = (
    # 中文指令词
    "你需要", "你要", "你的任务", "请", "按以下", "按下面", "按照",
    "应当", "必须", "禁止", "不要", "确保", "完成", "实现",
    "审查", "检查", "修复", "改写", "列出", "解释", "分析", "对比", "找出",
    # 英文指令词（祈使句常见动词 + Prompt 开头模式）
    "you must", "you should", "you need", "please ", "do not ", "ensure ",
    "make sure", "create ", "build ", "implement ",
    "your task", "your goal", "your job", "task:", "goal:", "objective:",
    "check ", "review ", "find ", "fix ", "list ", "explain ", "analyze ",
    "compare ", "identify ", "write ", "generate ", "produce ", "rewrite ",
    "refactor ", "optimize ", "convert ", "translate ", "summarize ",
)


def _is_doc_fragment(text: str) -> bool:
    """识别 AGENTS.md / CLAUDE.md / README 等文档的章节碎片。

    用户实测反馈：refresh-projects 自动扫项目 .md，把 "Coding Style & Naming Conventions"
    "Project Structure" 等纯陈述章节当提示词入库。这些没有指令性，是参考文档。
    """
    stripped = text.strip()
    first_chunk = stripped[:300].lower()
    n = len(stripped)

    # 1. 目录树字符 ≥ 5 个 → 八成是目录结构粘贴
    if len(_TREE_CHARS_RE.findall(stripped[:1500])) >= 5:
        return True

    # 2. 段首匹配文档前缀（纯英文文档专用判定）
    head_line = stripped.splitlines()[0].strip() if stripped.splitlines() else ""
    for prefix in _DOC_PREFIX_PHRASES:
        if first_chunk.startswith(prefix.lower()) or head_line.startswith(prefix):
            return True

    # 3. 章节标题指纹：原文本身或转入库时拼的 title 含已知章节名
    # （这层在 is_quality_prompt 入口走，所以只能从 body 里嗅）
    body_head = first_chunk
    title_hits = sum(1 for t in _DOC_CHAPTER_TITLES if t in body_head)
    if title_hits >= 1:
        # 还得没有指令性才算文档
        if not _has_imperative_voice(stripped):
            return True

    # 4. 长段（≥ 200 字）但无任何指令性动词 → 文档
    if n >= 200 and not _has_imperative_voice(stripped):
        # 排除 code-heavy（代码块占 > 50%）的，那不一定是文档
        code_chars = sum(
            len(line) for line in stripped.splitlines()
            if line.startswith("    ") or line.startswith("\t")
        )
        if code_chars / max(n, 1) < 0.50:
            return True

    return False


def _has_imperative_voice(text: str) -> bool:
    """检测是否有指令性动词（"你"/"请"/"按"/"必须"/"do"/"create" 等）。"""
    low = text.lower()
    return any(w.lower() in low for w in _IMPERATIVE_WORDS)


