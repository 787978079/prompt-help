"""提示词质量过滤：让挖到的"提示词"真的是用户写的、有用的、可复用的。

V1 实测发现的 5 类噪声：
  1. 乱码 / shell 日志 dump（PowerShell stderr 用 cp936 解码错误后存进 transcript）
  2. 一次性调试指令（"看 v1.6 e2e 日志（task b7a5nv6zh）..."—— 含具体 path/UUID/version，单次用）
  3. AI 回复风格泄漏（user role 但段首多为"我会..."、大段反引号包裹）
  4. 用户主语种不要的（中文用户的纯英文段、英文用户的纯中文段）
  5. 重复——差异极小的提示词重复入库

本模块是 5 层过滤的统一实现。配置项放在 cfg.quality（QualityConfig）。
"""

from __future__ import annotations

import difflib
import re
from dataclasses import dataclass
from typing import Callable, Iterable, TypeVar


# ---------------------------------------------------------------------------
# 配置
# ---------------------------------------------------------------------------

@dataclass
class QualityConfig:
    # 用户实测反馈：默认 both 让大量纯英文文档碎片混入；中文用户应默认 zh
    language_preference: str = "zh"     # zh | en | both
    min_chars: int = 100
    max_chars: int = 4000
    inter_dedupe_token: float = 0.55    # 候选间 token 重合度阈值
    inter_dedupe_seq_ratio: float = 0.80  # 候选间 SequenceMatcher 比阈值
    db_dedupe_token: float = 0.60       # 与库内 token 重合度阈值
    mojibake_strict: bool = True
    # 是否拒绝"文档碎片"（AGENTS.md / 编码规范的章节）。默认开。
    reject_doc_fragments: bool = True


# ---------------------------------------------------------------------------
# 入口：综合判定
# ---------------------------------------------------------------------------

NOISE_PREFIXES = (
    "<system-reminder>", "<command-", "<task-notification>",
    "[Image #", "[image #", "[Image:", "[image:", "[图片", "[Pasted text",
    # Claude Code 跑 !command 时注入的 envelope
    "<local-command-stdout>", "<local-command-stderr>",
    "<bash-input>", "<bash-stdout>", "<bash-stderr>",
    "Caveat:", "Caveat ",
)

# CC / 其他工具注入的 meta 指令短语（出现在任何位置都拒）
_TOOL_META_PHRASES = (
    "messages below were generated by the user while running local commands",
    "DO NOT respond to these messages or otherwise consider them",
    "Note: Reminder system has been removed",
    "<system>",
    "</system>",
)


def is_quality_prompt(text: str, qc: QualityConfig) -> tuple[bool, str | None]:
    """判定一段 user 文本是否值得入库。

    返回 (passed, reason)：reason 是 None 表示通过；否则是拒因短语，便于 dry-run 解释。
    """
    if not text or text.startswith("/"):
        return False, "empty_or_slash_command"
    text = text.strip()

    # 敏感信息（API key）—— 优先级最高，长度无关
    if _API_KEY_RE.search(text):
        return False, "contains_api_key"

    # 图片附件主体 —— 长度无关
    if _is_image_only(text):
        return False, "image_attachment_only"

    n = len(text)
    if n < qc.min_chars:
        return False, f"too_short_{n}_chars"
    if n > qc.max_chars:
        return False, f"too_long_{n}_chars"
    if any(text.startswith(p) for p in NOISE_PREFIXES):
        return False, "noise_prefix"

    # CC/工具的 meta 指令（不仅仅在开头，正文里出现也算）
    if any(phrase in text for phrase in _TOOL_META_PHRASES):
        return False, "tool_meta_injection"

    if _is_mojibake_or_log(text, strict=qc.mojibake_strict):
        return False, "mojibake_or_log_dump"

    if _is_error_log_paste(text):
        return False, "error_log_paste"

    if _is_oneoff_debug(text):
        return False, "oneoff_debug_instruction"

    if _is_ai_response_leak(text):
        return False, "looks_like_ai_response"

    if _violates_language_pref(text, qc.language_preference):
        return False, f"language_pref_{qc.language_preference}"

    if qc.reject_doc_fragments and _is_doc_fragment(text):
        return False, "doc_fragment"

    if not _has_min_structure_or_length(text):
        return False, "no_structure_and_short"

    return True, None


# ---------------------------------------------------------------------------
# 文档碎片识别（AGENTS.md / CLAUDE.md 章节、编码规范、目录树、README 段）
# ---------------------------------------------------------------------------
#
