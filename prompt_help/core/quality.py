T = TypeVar("T")

_TOKEN_RE = re.compile(r"[A-Za-z]{3,}|[一-鿿]{2,}")


def token_set(text: str) -> set[str]:
    return {t for t in _TOKEN_RE.findall(text.lower())}


def is_duplicate(
    a: str,
    b: str,
    *,
    token_threshold: float,
    seq_threshold: float,
) -> bool:
    """两条提示词是否近似重复。token 重合或 SequenceMatcher 任一命中即判重。"""
    ta = token_set(a)
    tb = token_set(b)
    if ta and tb:
        overlap = len(ta & tb) / max(min(len(ta), len(tb)), 1)
        if overlap >= token_threshold:
            return True
    # SequenceMatcher 慢，截到前 800 字
    sm = difflib.SequenceMatcher(None, a[:800], b[:800])
    return sm.ratio() >= seq_threshold


def dedupe_candidates(
    items: Iterable[T],
    get_text: Callable[[T], str],
    *,
    token_threshold: float = 0.55,
    seq_threshold: float = 0.80,
) -> list[T]:
    """候选间去重；同源候选保留长度最大的。"""
    kept: list[tuple[T, str]] = []
    for item in items:
        text = get_text(item)
        merged = False
        for i, (existing, existing_text) in enumerate(kept):
            if is_duplicate(
                text, existing_text,
                token_threshold=token_threshold,
                seq_threshold=seq_threshold,
            ):
                if len(text) > len(existing_text):
                    kept[i] = (item, text)
                merged = True
                break
        if not merged:
            kept.append((item, text))
    return [it for it, _ in kept]
