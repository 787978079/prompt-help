# Windows shell 错误在 cp936→UTF-8 错误解码后的特征
_MOJIBAKE_PHRASES = (
    "不是内部或外部命令",
    "鈥檚", "鈥檛",  # 常见 Windows-1252→UTF-8 误解码
    "锛?",  # cp936 中文逗号 mojibake
)
# CJK Extension B 起，常见用户写不出来的"罕用字"集合（mojibake 的高频区域）
_CJK_EXT_RE = re.compile(r"[㐀-䶿]")


def _is_mojibake_or_log(text: str, *, strict: bool = True) -> bool:
    """乱码 / 重复日志 dump 检测。

    判定规则（任一命中即为噪声）：
    1. 已知 shell 错误短语高频（"不是内部或外部命令" 等 ≥ 3 次）
    2. 罕用 CJK Extension B 字符占比过高（mojibake 特征）
    3. **重型重复**：最常见 7 字 phrase 覆盖文本 > 40%（log dump 特征；
       自然中文写作即使有重复段也不会到这个比例）
    """
    # 1. 已知错误短语高频
    for phrase in _MOJIBAKE_PHRASES:
        if text.count(phrase) >= 3:
            return True

    # 2. 罕用 CJK Extension B
    if strict:
        cjk_basic = sum(1 for c in text if "一" <= c <= "鿿")
        cjk_ext = len(_CJK_EXT_RE.findall(text))
        cjk_total = cjk_basic + cjk_ext
        if cjk_total >= 30 and cjk_ext / max(cjk_total, 1) > 0.05:
            return True

    # 3. 重型重复：最常见 7 字 phrase 覆盖文本 > 40%
    sample = text[:1500]
    if len(sample) >= 100:
        seen: dict[str, int] = {}
        max_count = 0
        for i in range(len(sample) - 7 + 1):
            seg = sample[i:i + 7]
            if "\n" in seg or seg.isspace():
                continue
            seen[seg] = seen.get(seg, 0) + 1
            if seen[seg] > max_count:
                max_count = seen[seg]
        if max_count >= 5 and max_count * 7 / len(sample) > 0.40:
            return True

    return False


# 一次性调试指令的特征
_WIN_PATH_RE = re.compile(r"[A-Za-z]:\\(?:[^\\\s\"<>|]+\\){4,}", re.UNICODE)
_BASE32_ID_RE = re.compile(r"\b[a-z][a-z0-9]{6,11}\b")  # 7-12 字符小写字母数字组合
_HEX_ID_RE = re.compile(r"\b[a-f0-9]{8,32}\b")
_VERSION_RE = re.compile(r"\bv\d+\.\d+(\.\d+)?\b")
_DEBUG_KEYWORDS_RE = re.compile(
    r"确认进度|重点验证|schedule\s+\d+\s*分钟|最近\s*\d+\s*行|最新.{0,10}行|"
    r"e2e\s+pipeline|pipeline\s+日志",
    re.IGNORECASE,
)
# API key 泄漏（绝对不入库，避免敏感信息）
_API_KEY_RE = re.compile(
    r"\bsk-[A-Za-z0-9_\-]{20,}|\bAKIA[0-9A-Z]{16}|\bgh[ps]_[A-Za-z0-9]{30,}",
)

# 图片附件标记
_IMAGE_BLOCK_RE = re.compile(r"\[[Ii]mage[^\]]*\]")

# error log / stack trace 粘贴的特征
_ERROR_LOG_PATTERNS = [
    re.compile(r"Error:\s+E[A-Z]+(?:\s|:)"),                 # Node.js: ENOENT/EACCES/EPERM
    re.compile(r"^\s*[Aa]t\s+[A-Za-z]:\\.*?:\d+", re.MULTILINE),  # PS: At D:\path:LN char:N
    re.compile(r"^\+\s+CategoryInfo\b", re.MULTILINE),
    re.compile(r"FullyQualifiedErrorId"),
    re.compile(r"ParserError\b"),
    re.compile(r"PowerShell\s+return\s+code"),
    re.compile(r"Traceback\s+\(most recent call last\)"),
    re.compile(r'^\s+File\s+".*?",\s+line\s+\d+', re.MULTILINE),  # Python stack
    re.compile(r"\berror\s+TS\d+:"),                              # TypeScript
    re.compile(r"^\s*at\s+\w[\w\.]*\s*\(.+:\d+:\d+\)", re.MULTILINE),  # JS stack
    re.compile(r"^\[cmd\]\s", re.MULTILINE),
    re.compile(r"^\s*\+\s*~~~+", re.MULTILINE),                   # PS error pointer ~~~~
    re.compile(r"^\w+:\s+真机调试\s*Error", re.MULTILINE),         # 微信小程序 error 日志
    re.compile(r"openid:\s*[A-Za-z0-9_\-]+|appid:\s*\w+"),         # 微信日志特征
]


def _is_image_only(text: str) -> bool:
    """主体是图片附件标记（含 [Image ...] 块且去掉后剩不到 30 字）→ 拒。"""
    if not _IMAGE_BLOCK_RE.search(text):
        return False
    cleaned = _IMAGE_BLOCK_RE.sub("", text).strip()
    return len(cleaned) < 30


def _is_error_log_paste(text: str) -> bool:
    """用户从控制台粘贴的 error / stack trace 日志（不是真要复用的提示词）。

    判定：≥ 2 个 error log 模式命中 → 拒。
    （单个命中可能是模板里提到错误名，2 个以上才像真的粘了一段栈）
    """
    hits = sum(1 for p in _ERROR_LOG_PATTERNS if p.search(text))
    return hits >= 2


def _is_oneoff_debug(text: str) -> bool:
    """一次性调试指令检测。

    判定（放宽后，只拒最明显的一次性指令）：
    - 含 API key → 直接拒（任何长度）
    - 信号数 ≥ 3 且长度 < 800 → 拒（短到中等长度 + 多种具体痕迹 = 真一次性）
    - 信号数 ≥ 4 → 拒（长度无关；4+ 信号几乎不可能是模板）
    单个或两个信号一律放过（容错给用户）。
    """
    if _API_KEY_RE.search(text):
        return True

    signals = 0
    if _WIN_PATH_RE.search(text):
        signals += 1
    if _BASE32_ID_RE.search(text) and re.search(r"\btask\s|（task\b", text, re.IGNORECASE):
        signals += 1
    if _HEX_ID_RE.search(text):
        signals += 1
    if _VERSION_RE.search(text):
        signals += 1
    if _DEBUG_KEYWORDS_RE.search(text):
        signals += 1

    if signals >= 4:
        return True
    if signals >= 3 and len(text) < 800:
        return True
    return False


# AI 回复的开头语和风格特征
_AI_OPENERS = (
    "我会", "我理解", "我已", "OK，让我", "好的，让我", "根据你的", "根据上述",
    "让我帮你", "让我们", "Let me", "I'll ", "I will ", "I'll go ", "Sure, ",
    "Here's", "Here is",
)


def _is_ai_response_leak(text: str) -> bool:
    """user 消息但风格像 AI 回复。"""
    lines = [line.strip() for line in text.splitlines() if line.strip()]
    if len(lines) < 2:
        return False

    # 段首多为 AI opener
    ai_lines = sum(1 for line in lines if any(line.startswith(o) for o in _AI_OPENERS))
    if ai_lines >= 3 and ai_lines / len(lines) > 0.30:
        return True

    # 代码块占比 > 70%
    in_code = False
    code_chars = 0
    for line in text.splitlines():
        s = line.strip()
        if s.startswith("```"):
            in_code = not in_code
            continue
        if in_code:
            code_chars += len(line)
    if code_chars / max(len(text), 1) > 0.70:
        return True

    # ✓✅ 列表项密度高
    list_lines = [line for line in lines if line[:2] in ("- ", "* ") or line[0] in "•✓✅☑"]
    if list_lines:
        check_lines = sum(
            1 for line in list_lines
            if any(c in line[:5] for c in "✓✅☑")
        )
        if check_lines / len(list_lines) > 0.70 and len(list_lines) >= 3:
            return True

    return False


def _violates_language_pref(text: str, pref: str) -> bool:
    """主语种过滤（放宽后只拒纯外语长段）。"""
    if pref == "both":
        return False
    cjk_count = sum(1 for c in text if "一" <= c <= "鿿")
    ascii_letters = sum(1 for c in text if c.isascii() and c.isalpha())
    total = cjk_count + ascii_letters
    if total < 50:
        return False  # 太短判不出主语种
    cjk_ratio = cjk_count / total
    if pref == "zh":
        # 中文用户：纯英文且长度可观才拒（CJK 几乎为 0 + 至少 150 个英文字母）
        return cjk_ratio < 0.02 and ascii_letters > 150
    if pref == "en":
        return cjk_ratio > 0.70
    return False


# ---------------------------------------------------------------------------
# 结构 / 闲聊词过滤（来自旧 _is_quality_prompt 的逻辑）
# ---------------------------------------------------------------------------

_STRUCT_PATTERNS = [
    re.compile(r"^\s*[-*]\s+", re.MULTILINE),
    re.compile(r"^\s*\d+[\.、]\s+", re.MULTILINE),
    re.compile(r"```"),
    re.compile(r"<task>|<context>|<constraints>|<output_format>", re.IGNORECASE),
    re.compile(r"你的任务|按以下步骤|你需要|要求如下|请按照|step\s*\d", re.IGNORECASE),
]

_FLOW_WORDS = (
    "继续", "嗯", "好", "好的", "ok", "OK", "可以", "对的", "不对", "再来",
    "不要", "不行", "再说", "嗯嗯", "知道了", "明白", "请",
)


def _has_min_structure_or_length(text: str) -> bool:
    """要么有结构标记，要么够长。同时排除"嗯/好"开头的闲聊。

    放宽规则：
    - 中文密度高，一句话 100 字就算合格内容
    - 英文 / 拉丁字母为主时阈值放宽到 180
    """
    first_line = text.splitlines()[0].strip() if text.splitlines() else ""
    if first_line and len(first_line) < 8 and any(first_line.startswith(w) for w in _FLOW_WORDS):
        return False
    structure_count = sum(1 for p in _STRUCT_PATTERNS if p.search(text))
    if structure_count >= 1:
        return True
    cjk_count = sum(1 for c in text if "一" <= c <= "鿿")
    if cjk_count >= 80:  # 中文 ≥ 80 字 ≈ 完整一句话指令
        return True
    return len(text) >= 180


