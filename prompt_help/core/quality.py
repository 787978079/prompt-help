LLM_JUDGE_SYSTEM_PROMPT = """\
你是提示词审核员。判断下面这段文字是不是"用户写的、可让 AI 复用的提示词"。

是提示词的特征：
- 含明确指令（让 AI 做某事：写代码、审查、改写、列出、解释…）
- 有可复用性（不仅仅描述一次性场景）
- 用户写给 AI 看的（而非 AI 回复 / 项目文档 / 错误日志）

不是提示词的特征：
- AGENTS.md / CLAUDE.md 章节碎片（编码规范、项目结构等）
- README 段落 / 项目介绍
- 错误日志 / shell 输出 / 一次性调试指令
- 目录树 / ASCII 图表
- AI 回复的开头语（"我会…"、"OK, let me…"）

只输出一个字：Y（是提示词）或 N（不是）。不要任何解释。
"""


def llm_judge_is_prompt(cfg, text: str) -> tuple[bool, str]:
    """调 LLM 二次判定（仅在规则层放过 + 用户启用时调）。

    返回 (is_prompt, raw_response)。失败时返回 (True, error)——保守放过避免误删。
    """
    try:
        from . import optimizer
        # 截到前 1200 字给 LLM 判断（节省 token）
        sample = text[:1200]
        result = optimizer._run(
            cfg, sample,
            system_prompt=LLM_JUDGE_SYSTEM_PROMPT,
            mode="auto",
            wrap_user_in_xml=True,
        )
        if not result.success or not result.optimized:
            return True, result.error or "empty"
        verdict = result.optimized.strip().upper()[:5]
        is_prompt = verdict.startswith("Y") or "YES" in verdict
        return is_prompt, verdict
    except Exception as e:
        return True, f"err:{type(e).__name__}"


